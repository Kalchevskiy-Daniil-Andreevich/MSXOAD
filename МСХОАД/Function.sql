CREATE OR REPLACE FUNCTION RAND_PHONE_NUMBER
RETURN NVARCHAR2
IS 
COUNTRY_CODE NVARCHAR2(4) := '+375';
OPERATOR_CODE NVARCHAR2(2);
PHONE_CODE NVARCHAR2(50);
PHONE_NUMBER NVARCHAR2(100);
BEGIN
SELECT NUMBER_O INTO OPERATOR_CODE FROM (
SELECT NUMBER_O FROM OPER
ORDER BY DBMS_RANDOM.RANDOM)
FETCH FIRST 1 ROW ONLY;
SELECT DBMS_RANDOM.VALUE(1000000, 9999999) NUM INTO PHONE_CODE FROM DUAL;
PHONE_CODE := ROUND(PHONE_CODE);
PHONE_NUMBER := CONCAT(CONCAT(COUNTRY_CODE, OPERATOR_CODE), PHONE_CODE);
RETURN PHONE_NUMBER;
END;


