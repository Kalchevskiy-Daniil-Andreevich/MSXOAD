CREATE OR REPLACE FUNCTION RPASSPORT
RETURN NVARCHAR2
IS
LPASSPORT NVARCHAR2(2);
NPASSPORT NUMBER;
FPASSPORT NVARCHAR2(9);
BEGIN 
SELECT LETTERS INTO LPASSPORT FROM(
SELECT LETTERS FROM DLPASSPORT
WHERE LETTERS IS NOT NULL
ORDER BY DBMS_RANDOM.RANDOM)
FETCH FIRST 1 ROW ONLY;
SELECT DBMS_RANDOM.VALUE(0000000, 9999999) NUM INTO NPASSPORT FROM DUAL;
NPASSPORT := ROUND(NPASSPORT);
FPASSPORT := CONCAT(LPASSPORT, NPASSPORT);
RETURN FPASSPORT;
END;

