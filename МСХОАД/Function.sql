CREATE OR REPLACE FUNCTION RAND_CITY
RETURN NVARCHAR2
IS 
FCITY NVARCHAR2(30);
BEGIN
SELECT CITY INTO FCITY FROM(
SELECT CITY FROM ADDRESS_INFO
WHERE CITY IS NOT NULL
ORDER BY DBMS_RANDOM.RANDOM)
FETCH FIRST 1 ROW ONLY;
RETURN FCITY;
END;
/
CREATE OR REPLACE FUNCTION RAND_STREET(FCITY NVARCHAR2)
RETURN NVARCHAR2
IS 
FSTREET NVARCHAR2(30);
BEGIN
SELECT STREET INTO FSTREET FROM(
SELECT STREET FROM ADDRESS_INFO
WHERE CITY LIKE FCITY
ORDER BY DBMS_RANDOM.RANDOM)
FETCH FIRST 1 ROW ONLY;
RETURN FSTREET;
END;
/
CREATE OR REPLACE FUNCTION RAND_ADDRESS 
RETURN NVARCHAR2
IS
ADDRESS NVARCHAR2(100);
RNUM NUMBER;
FCITY NVARCHAR2(50);
BEGIN
FCITY := RAND_CITY();
SELECT DBMS_RANDOM.VALUE(1, 100) NUM INTO RNUM FROM DUAL;
RNUM := ROUND(RNUM);
ADDRESS := CONCAT('г. ', CONCAT(FCITY, CONCAT(', ', CONCAT(RAND_STREET(FCITY), CONCAT(' ', RNUM)))));
RETURN ADDRESS;
END;

